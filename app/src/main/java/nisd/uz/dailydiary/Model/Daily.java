package nisd.uz.dailydiary.Model;

public class Daily {
    int kid;
    String sana, description,title;

    public Daily() {
        this.kid = kid;
        this.sana = sana;
        this.description = description;
        this.title = title;
    }

    public int getKid() {
        return kid;
    }

    public void setKid(int kid) {
        this.kid = kid;
    }

    public String getSana() {
        return sana;
    }

    public void setSana(String sana) {
        this.sana = sana;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
